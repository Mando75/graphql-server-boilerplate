import { ApolloServer, gql } from "apollo-server";

const books = [
  {
    title: "An example book",
    author: "dickweed1"
  }
];

const typeDefs = gql`
  type Book {
    title: String
    author: String
  }

  type Query {
    books: [Book]
  }
`;

const resolvers = {
  Query: {
    books: () => books
  }
};

const server = new ApolloServer({ typeDefs, resolvers });

server.listen().then(({ url }: { url: string }) => {
  console.log(`ğŸš€  Server ready at ${url}`);
});
